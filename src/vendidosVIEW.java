    public void listarTblVendidos(String statusVenda) {

        boolean status = conexao.connectDB();

        if (status == false) {

            JOptionPane.showMessageDialog(null,"Erro ao conectar com Banco de Dados","Banco de Dados", JOptionPane.ERROR_MESSAGE);

        } else {

            List<ProdutosDTO> listProduto = produtosDAO.listaTabela(statusVenda);

            tabelaVendido = (DefaultTableModel) tabelaVendidos.getModel();

            tabelaVendidos.setRowSorter(new TableRowSorter(tabelaVendido));
            tabelaVendido.setNumRows(0);

            for (ProdutosDTO produto : listProduto) {

                Object[] obj = new Object[]{produto.getId(), produto.getNome(), produto.getValor(),
                    produto.getStatus()};

                tabelaVendido.addRow(obj);

            }

            conexao.desconectarDB();
        }

    }

